package com.tw.datatype;

public class Relationoperator {

	public static void main(String[] args) {
		
      //		[==.!=,<,<=,>,>,>,>=]
		
     //	You can also test for true or false values with logical operators.

		
		// Initializing variables
		
		int junaid =5;
		int bilal =10;
		int zeeshan =15;
	
		
        
        System.out.println(junaid==bilal);
        
        //var1 nahi hai var2 ke  barabar
        System.out.println(junaid != bilal);
      
        System.out.println(zeeshan >= bilal);
		
		
        System.out.println(junaid  <=  zeeshan);
        System.out.println(zeeshan <= junaid );
		
		System.out.println(junaid >zeeshan );
		
		System.out.println(zeeshan=junaid);
	} 

}
